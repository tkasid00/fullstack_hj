public class Re0911_2 {
	public static void main(String[] args) {
	

		for(int i=1;i<=5;i++) {System.out.print(i + "\t");}
		
		System.out.println();
		
		int j=1;
		while(j<=5) {System.out.print(j + "\t"); j++;}
		
		System.out.println();
		
		int k = 1;
		do{System.out.print(k + "\t"); k++;} while(k<=5); 
		
		
	}

}
