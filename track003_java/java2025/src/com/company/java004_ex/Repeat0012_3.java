package com.company.java004_ex;

public class Repeat0012_3 {
	public static void main(String[] args) {
		int i = 0;
		
		for(i=1; i<=5; i++) {System.out.print(i + "\t");}
		System.out.println();
		for(i=5; i>=1; i--) {System.out.print(i + "\t");}
		System.out.println();
		for(i=1; i<=3; i++) {System.out.print("JAVA" + i + "\t");}
		
	}

}
