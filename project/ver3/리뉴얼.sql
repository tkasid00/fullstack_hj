  ADMINID        NUMBER          PRIMARY KEY,
  EMAIL          VARCHAR2(100) NOT NULL,
  PASSWORD       VARCHAR2(255) NOT NULL,
  ROLE           VARCHAR2(20) DEFAULT 'ADMIN',
  CREATEDAT      DATE DEFAULT SYSDATE
);


